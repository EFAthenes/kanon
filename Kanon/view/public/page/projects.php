                            <div class="col-lg-10">

                            <p>
                                
                                Launched online in 2011, the Archimage database, dedicated to the internal management of the photographic and graphic archives of the EFA, also aimed to make a selection of documents available online in digitized form. Containing to date more than 670,000 records describing documents item by item, the database required a technological update as well as a partial revision of its content.
                            </p>

                            <p>
                                Alongside this database, other existing applications were used for describing the collections. Thus, the inventories of manuscript archives had been imported into an AtoM database and the squeezes were described in a FileMaker Pro database, both consultable internally.
                            </p>

                            <p>
                                Finally, the development of digital humanities required support for new composite data, such as 3D renderings, RTI, etc.
                            </p>


                            <p>
                                In light of this observation, it was decided to develop a database serving as a single interface for consulting and disseminating the EFA archives, across all types and media, relying on the latest available technologies, within a standards-based approach and in a spirit of opening research data.
                            </p>

                            
                            
