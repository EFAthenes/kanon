The Publications of the French School at Athens (EFA) remain an essential reference for anyone interested in Greece and the Greek world, from Antiquity to the present day. They offer more than 600 titles covering history, archaeology, epigraphy, numismatics, geography, sociology, and, more broadly, all scholarly disciplines related to the study of Antiquity, as well as several fields within the humanities and social sciences.

Faithful to their original mission of disseminating the research conducted by the institution and its scientific partners, the Publications of the French School at Athens release between twenty and twenty-five new titles each year. Widely distributed throughout the French-speaking world and internationally, these publications are intended not only for academic audiences but also for the general public.

