                            Archimage
                        </h2>
                        <div class="row">  
                             <div class="col-lg-2">
                                <a href="https://archimage.efa.gr" target="_blank">
                                <img class="img-fluid" src="https://archimage.efa.gr/img/logo/archimage_projet_logo.jpg" alt="Archimage">
